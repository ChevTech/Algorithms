/**
 * @param {character[][]} board
 * @param {string} word
 * @return {boolean}
 */
var exist = function(board, word) {
    var found = false;
    var used = new Set();
    for(var i=0; i < board.length; i++){
        for(var j=0; j < board[0].length; j++){
            var firstChar = word.charAt(0);
            if(board[i][j] === firstChar){
                used.add(`${i}-${j}`);
                bt(word, firstChar, i, j, used);
                used.delete(`${i}-${j}`);
            }
        }
    }
    
    function bt(word, str, indexI, indexJ, used){
        if(str === word){
            found = true;
            return;
        }
        
        if(str.length > word.length){
            return;
        }
        
        var nextChar = word.charAt(str.length);
        var choices = getChoices(board, indexI, indexJ, nextChar, used);
        for(var i=0; i < choices.length; i++){
            var newI = choices[i][0];
            var newJ = choices[i][1];
            str = str.concat(board[newI][newJ]);
            used.add(`${newI}-${newJ}`);
            bt(word, str, newI, newJ, used);
            str = str.substring(0, str.length-1);
            used.delete(`${newI}-${newJ}`);
        }
    }

    function getChoices(board, x, y, nextStr, used){
        var up = x-1;
        var down = x+1;
        var left = y-1;
        var right = y+1;   
        
        var res = [];
        if(up >= 0 && board[up][y] === nextStr && !used.has(`${up}-${y}`)){
            res.push([up, y]);
        }    
        if(down < board.length && board[down][y] === nextStr && !used.has(`${down}-${y}`)){
            res.push([down, y]);
        }    
        if(left >= 0 && board[x][left] === nextStr && !used.has(`${x}-${left}`)){
            res.push([x, left]);
        }    
        if(right < board[0].length && board[x][right] === nextStr && !used.has(`${x}-${right}`)){
            res.push([x, right]);
        }  
        return res;
    };
    return found;
};
